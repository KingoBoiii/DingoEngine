// Low-poly dungeon-crawler character generator (per-part meshes).
//
// Emits one Wavefront OBJ per body part (flat per-face normals) that DingoEngine loads
// via Model::LoadFromFile (Assimp). The engine has no skeletal animation (the loader
// bakes transforms), so the character is assembled from separate part *entities* and
// animated procedurally in code (see Character.{h,cpp}); keeping each part as its own
// mesh is what lets the limbs and the sword move independently.
//
// Each part is authored centered on its LOCAL origin (so the rig can position + rotate
// it about a joint), EXCEPT the sword, whose origin is at the grip with the blade
// pointing +Y, so it pivots naturally from the hand. All parts face +Z. Units = meters.
//
// Run:  node generate_models.js
// Out:  parts/{head,torso,arm,leg,sword}.obj

const fs = require("fs");
const path = require("path");

class ObjBuilder {
	constructor() {
		this.V = [];
		this.N = [];
		this.F = [];
	}

	sub(a, b) { return [a[0] - b[0], a[1] - b[1], a[2] - b[2]]; }
	cross(a, b) {
		return [a[1] * b[2] - a[2] * b[1], a[2] * b[0] - a[0] * b[2], a[0] * b[1] - a[1] * b[0]];
	}
	norm(v) {
		const l = Math.hypot(v[0], v[1], v[2]) || 1;
		return [v[0] / l, v[1] / l, v[2] / l];
	}

	// Triangle with a computed flat normal (CCW front).
	tri(a, b, c) {
		const n = this.norm(this.cross(this.sub(b, a), this.sub(c, a)));
		const base = this.V.length;
		this.V.push(a, b, c);
		const ni = this.N.length;
		this.N.push(n);
		this.F.push([[base + 0, ni], [base + 1, ni], [base + 2, ni]]);
	}

	// Quad (4 CCW corners) with one flat normal.
	quad(a, b, c, d, n) {
		const base = this.V.length;
		this.V.push(a, b, c, d);
		const ni = this.N.length;
		this.N.push(n);
		this.F.push([[base + 0, ni], [base + 1, ni], [base + 2, ni]]);
		this.F.push([[base + 0, ni], [base + 2, ni], [base + 3, ni]]);
	}

	// Axis-aligned box by center + full size. Winding matches Mesh::CreateBox.
	box(cx, cy, cz, sx, sy, sz) {
		const x0 = cx - sx / 2, x1 = cx + sx / 2;
		const y0 = cy - sy / 2, y1 = cy + sy / 2;
		const z0 = cz - sz / 2, z1 = cz + sz / 2;
		this.quad([x0, y0, z1], [x1, y0, z1], [x1, y1, z1], [x0, y1, z1], [0, 0, 1]);
		this.quad([x1, y0, z0], [x0, y0, z0], [x0, y1, z0], [x1, y1, z0], [0, 0, -1]);
		this.quad([x1, y0, z1], [x1, y0, z0], [x1, y1, z0], [x1, y1, z1], [1, 0, 0]);
		this.quad([x0, y0, z0], [x0, y0, z1], [x0, y1, z1], [x0, y1, z0], [-1, 0, 0]);
		this.quad([x0, y1, z1], [x1, y1, z1], [x1, y1, z0], [x0, y1, z0], [0, 1, 0]);
		this.quad([x0, y0, z0], [x1, y0, z0], [x1, y0, z1], [x0, y0, z1], [0, -1, 0]);
	}

	// A pyramid apex over a rectangle at height yBase (4 slanted faces).
	pyramidTip(cx, cz, hx, hz, yBase, apexY) {
		const a = [cx - hx, yBase, cz + hz];
		const b = [cx + hx, yBase, cz + hz];
		const c = [cx + hx, yBase, cz - hz];
		const d = [cx - hx, yBase, cz - hz];
		const apex = [cx, apexY, cz];
		this.tri(a, b, apex);
		this.tri(b, c, apex);
		this.tri(c, d, apex);
		this.tri(d, a, apex);
	}

	toObj(name) {
		const f = (v) => v.map((c) => c.toFixed(5)).join(" ");
		let out = `# ${name} — low-poly character part (DingoEngine)\n`;
		out += `# Generated by generate_models.js. Faces +Z. Units = meters.\no ${name}\n`;
		for (const v of this.V) out += `v ${f(v)}\n`;
		for (const n of this.N) out += `vn ${f(n)}\n`;
		for (const t of this.F)
			out += `f ${t.map(([vi, ni]) => `${vi + 1}//${ni + 1}`).join(" ")}\n`;
		return out;
	}
}

// ---- Parts (centered on local origin, except the sword) -------------------

function head() {
	const o = new ObjBuilder();
	o.box(0, 0, 0, 0.34, 0.34, 0.34);
	// Brow ridge / face hint on the +Z side so facing reads.
	o.box(0, -0.02, 0.18, 0.22, 0.06, 0.04);
	return o;
}

function torso() {
	const o = new ObjBuilder();
	o.box(0, 0, 0, 0.46, 0.42, 0.28);    // chest
	o.box(0, -0.20, 0, 0.34, 0.06, 0.24); // belt
	return o;
}

function arm() {
	const o = new ObjBuilder();
	o.box(0, 0, 0, 0.13, 0.42, 0.15);
	return o;
}

function leg() {
	const o = new ObjBuilder();
	o.box(0, 0.02, 0, 0.17, 0.41, 0.20); // shin/thigh
	o.box(0, -0.205, 0.04, 0.18, 0.08, 0.26); // foot (forward, +Z)
	return o;
}

// Sword: origin at the grip, blade pointing +Y, slight +Z offset (held in front).
function sword() {
	const o = new ObjBuilder();
	const z = 0.0;
	o.box(0, -0.05, z, 0.035, 0.16, 0.035); // grip
	o.box(0, -0.15, z, 0.06, 0.05, 0.06);   // pommel
	o.box(0, 0.04, z, 0.24, 0.05, 0.07);    // crossguard
	o.box(0, 0.33, z, 0.06, 0.50, 0.03);    // blade (y: 0.08 .. 0.58)
	o.pyramidTip(0, z, 0.03, 0.015, 0.58, 0.66); // pointed tip
	return o;
}

const dir = __dirname;
const outDir = path.join(dir, "parts");
fs.mkdirSync(outDir, { recursive: true });

const parts = {
	head: head(),
	torso: torso(),
	arm: arm(),
	leg: leg(),
	sword: sword(),
};

for (const [name, obj] of Object.entries(parts)) {
	fs.writeFileSync(path.join(outDir, `${name}.obj`), obj.toObj(name));
	console.log(`parts/${name}.obj: ${obj.V.length} verts, ${obj.F.length} tris`);
}
console.log("Done.");
